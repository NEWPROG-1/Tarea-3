

# Definir variable para la base del rectangulo, se le pide al usuario

base = float(input("Ingrese el valor de la base (Anchura) del rectángulo: "))

# Definir varable para la altura del rectangulo, se le pide al usuario


altura = float(input("Ingrese el valor de la altura del rectángulo: "))

# operacion para hallar el area


area = base * altura 

# imprimir el resultado del area

 
print("El área del rectangulo es: ", area, "metros cuadrados")